CIPHERTEXT = bytes.fromhex("9e4647dc0affbb3b65a21037261b2123")


def add_scaled(total, row, sign):
    return tuple((x + sign * y) % P for x, y in zip(total, row))


def signed_sums(rows, base_index, choose):
    zero = (0,) * len(rows[0])
    for comb in itertools.combinations(range(len(rows)), choose):
        for signs in itertools.product((1, -1), repeat=choose):
            total = zero
            part = []
            for local_idx, sign in zip(comb, signs):
                total = add_scaled(total, rows[local_idx], sign)
                part.append((base_index + local_idx, sign))
            yield total, part


def recover_h():
    m = len(G)
    mid = m // 2
    left = G[:mid]
    right = G[mid:]
    left_weight = W // 2
    right_weight = W - left_weight

    table = defaultdict(list)
    for total, part in signed_sums(left, 0, left_weight):
        table[total].append(part)

    for total, right_part in signed_sums(right, mid, right_weight):
        target = tuple((-x) % P for x in total)
        for left_part in table.get(target, []):
            h = [0] * m
            for idx, sign in left_part + right_part:
                h[idx] = sign
            if all(sum(h[i] * G[i][k] for i in range(m)) % P == 0 for k in range(len(G[0]))):
                return h

    raise ValueError("no h found")


def stream_bytes(h, length):
    material = (
        "Curve_Link_Task1_Hard|P=65537|w=10|h="
        + ",".join(map(str, h))
    ).encode()
    out = b""
    counter = 0
    while len(out) < length:
        out += hashlib.sha256(material + counter.to_bytes(4, "big")).digest()
        counter += 1
    return out[:length]


def decrypt_with_h(h):
    stream = stream_bytes(h, len(CIPHERTEXT))
    return bytes(c ^ s for c, s in zip(CIPHERTEXT, stream))


def main():
    h = recover_h()

